def NtoC(n) :
    if (n == 1) :
        return "A"
    elif (n == 2) :
        return "Am"
    elif (n == 3):
        return "Bm"
    elif (n == 4):
        return "C"
    elif (n == 5):
        return "D"
    elif (n == 6):
        return "Dm"
    elif (n == 7):
        return "E"
    elif (n == 8):
        return "Em"
    elif (n == 9):
        return "F"
    elif (n == 10) :
        return "G"

